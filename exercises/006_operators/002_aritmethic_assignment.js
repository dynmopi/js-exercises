/*
operadores aritmeticos: sao as operacoes classicas da matematica...
operadores de declaracao: aquelas que declaram as variaveis
*/

//aritmeticos
let a = 4;
let x = (100 + 50) * a;
console.log(x); // retorna 600

/*
    +	Addition
    -	Subtraction
    *	Multiplication
    **	Exponentiation (ES2016)
    /	Division
    %	Modulus (Division Remainder) ou (Resto da divisao)
    ++	Increment (x++ -> x = x + 1)
    --	Decrement (x-- -> x = x - 1)
*/

//declarativos, uma forma de simplificar o codigo
let n = 10;
n += 10;

/*
assignment operators
=	x = y	x = y
+=	x += y	x = x + y
-=	x -= y	x = x - y
*=	x *= y	x = x * y
/=	x /= y	x = x / y
%=	x %= y	x = x % y
**=	x **= y	x = x ** y

*/
